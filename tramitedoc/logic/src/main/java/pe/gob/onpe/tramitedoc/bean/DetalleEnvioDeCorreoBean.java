    private String motivo;
    private String docorigen;
    private String docrespuesta;
    private String tipodoc;

    /**
     * @return the codtipoexp
     */
    public String getCodtipoexp() {
        return codtipoexp;
    }

    /**
     * @param codtipoexp the codtipoexp to set
     */
    public void setCodtipoexp(String codtipoexp) {
        this.codtipoexp = codtipoexp;
    }

    /**
     * @return the autcorreo
     */
    public String getAutcorreo() {
        return autcorreo;
    }

    /**
     * @param autcorreo the autcorreo to set
     */
    public void setAutcorreo(String autcorreo) {
        this.autcorreo = autcorreo;
    }

    /**
     * @return the correoexp
     */
    public String getCorreoexp() {
        return correoexp;
    }

    /**
     * @return the docrespuesta
     */
    public String getDocrespuesta() {
        return docrespuesta;
    }

    /**
     * @param docrespuesta the docrespuesta to set
     */
    public void setDocrespuesta(String docrespuesta) {
        this.docrespuesta = docrespuesta;
    }

